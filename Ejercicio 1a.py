"""Programa que pide por teclado dos numeros y muestra por pantalla 
su suma y su resta"""

#Declaracion de Variables.
numero1= 0 #Contiene el primer valor que pido por teclado
numero2= 0 #Contiene el segundo valor que pido por teclado

#Programa principal.
numero1=int(input("introduce el primer numero: ")) #Pedir numero y cambiarlo a numeral con Int
numero2=int(input("Introduce el segundo numero: "))

print("La suma es ", numero1+numero2) #Sumo las dos variables y las muestro
print("La resta es ", numero1-numero2) #Resto las dos variables y las muestro